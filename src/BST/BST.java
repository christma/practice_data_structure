    private Node add1(Node node, E e) {
        if (node == null) {
            size++;
            return new Node(e);
        }
        if (e.compareTo(node.e) < 0) {
            node.left = add1(node.left, e);
        } else if (e.compareTo(node.e) > 0) {
            node.right = add1(node.right, e);
        }
        return node;
    }
